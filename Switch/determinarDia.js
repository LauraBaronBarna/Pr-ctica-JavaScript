//Crear un programa que me permita identificar el día de la semana

const numeroDia = 5;

//Inicializamos el menú de opciones
switch(numeroDia){
    case 1:
        console.log("Lunes");
        break;
        case 2:
            console.log("Martes");
            break;
            case 3:
                console.log("Miércoles");
                break;
                case 4:
                    console.log("Jueves");
                    break;
                    case 5:
                        console.log("Viernes");
                        break;
                        case 6:
                            console.log("Sábado");
                            break;
                            case 7:
                                console.log("Domingo");
                                break;
                                default:
                                    console.log("El número de día que ingresaste no es válido");    
}