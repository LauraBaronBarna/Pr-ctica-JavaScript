//Crear un programa que ayude a determinar el tipo de vehículo

const tipo ="motocicleta";

switch(tipo){
    case "motocicleta":
        console.log("Esta es una motocicleta");
        break;
        case "camión":
            console.log("Este es un camión");
            break;
            case "helicóptero":
                console.log("Este es un helicóptero");
                break;
                default: console.log("No se reconoce el tipo de vehículo");      
}